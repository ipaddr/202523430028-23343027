void main() {
  var age = 20;
  var halfAge = age / 2; // Hasilnya double
  var doubleAge = age * 2;

  print('Umur saya adalah $age');
  print('Setengah umur saya adalah $halfAge');
  print('Dua kali umur saya adalah $doubleAge');
  print(
    'Dua kali umur saya adalah ${age * 2}',
  ); // Menggunakan kurung kurawal untuk ekspresi
}
